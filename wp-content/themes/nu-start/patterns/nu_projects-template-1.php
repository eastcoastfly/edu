<div class="wp-block-group"><!-- wp:paragraph -->
<p>When the SARS-CoV-2 virus hit, OHDSI members accustomed to the relatively slow pace of studying chronic disease proved their mettle against acutely infectious COVID-19. In a four-day marathon in March 2020, 330 volunteers from 30 countries came together to ask and answer questions during a virtual “study-a-thon,” drawing initially on patient data from South Korea and the U.K.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>How did the virus spread, and how quickly? Which people were at greatest risk of hospitalization and death? What available treatments were safe and effective? Which were pointless, even dangerous?&nbsp;</p>
<!-- /wp:paragraph --></div>
<!-- /wp:group -->

<!-- wp:group {"layout":{"type":"constrained","justifyContent":"left"}} -->
<div class="wp-block-group"><!-- wp:paragraph -->
<p><strong>Among the key findings from 2020:&nbsp;</strong></p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Early on, insufficient and low-quality data led to the use of ineffective medications (e.g., hydroxychloroquine used with popular antibiotic azithromycin induced heart failure)&nbsp;</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Compared with flu, COVID-19 led to more respiratory and other complications and higher mortality rates&nbsp;</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>Obese patients had higher hospitalization rates and required more intensive services&nbsp;</li>
<!-- /wp:list-item -->

<!-- wp:list-item -->
<li>In patients with a history of cancer, COVID-19 led to a host of complications&nbsp;</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list --></div>
<!-- /wp:group -->

<!-- wp:group {"layout":{"type":"constrained","justifyContent":"left"}} -->
<div class="wp-block-group"><!-- wp:paragraph -->
<p>The research continues, refocused on vaccines and long-term complications, notes Kristin Kostka, now the OHDSI Center’s director. Kostka is using machine learning to characterize long COVID syndromes. Meanwhile, Executive Director Christian Reich is expanding open-source ontologies to harmonize vaccine and long-COVID data. And Postdoctoral Fellow Louisa Smith is refining statistical methods for comparing the safety of vaccines.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>The OHDSI community has long experience studying chronic diseases like diabetes and cancer. With SARS-CoV-2 and its variants, Kostka says, “We’ve had to rise to the new challenges of acute infection,” working with lightning speed to mine insights from the data flooding in. To study COVID-19 at scale, she says, she and Reich worked with the NIH to harmonize “massive” amounts of electronic health data.&nbsp;</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Northeastern offers unique opportunities to study COVID-19, Kostka notes. The university’s rigorous testing protocol for students, staff, faculty, and visitors early in the pandemic has resulted in a wealth of data, including what she calls one of the world’s largest continually updated cohort of asymptomatic disease carriers.&nbsp;</p>
